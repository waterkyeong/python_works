# 원래 설정되어 있는 알람을 45분 앞서는 시간으로 바꾸는 것이다. 첫째 줄에 두 정수 H와 M이 주어진다. (0 ≤ H ≤ 23, 0 ≤ M ≤ 59) 그리고 이것은 현재 상근이가 설정한 알람 시간 H시 M분을 의미한다.

# 입력 시간은 24시간 표현을 사용한다. 24시간 표현에서 하루의 시작은 0:0(자정)이고, 끝은 23:59(다음날 자정 1분 전)이다. 시간을 나타낼 때, 불필요한 0은 사용하지 않는다.

#H : M 인데,,, -45분이고 그러면 M에서 45를 빼고 만약 M<45이면 45-M H-1 60-(45-M) = 60-45+M =15+M... 최종 시간이됨. 만약 M>45 이면 H:M-45이다. 수식으로 바꿔보자

H,M = map(int,input().split())

if M < 45:
    M = M+15
    if H == 0:
        H = 23
    else:
        H = H-1
else:
    M = M-45
    
print(H,M)